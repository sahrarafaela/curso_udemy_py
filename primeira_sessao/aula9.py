## operadores aritméticos

adicao = 10 + 10;
print('Adição', adicao);

subtracao = 10 - 10;
print('Subtração', subtracao);

multiplicacao = 10 * 10;
print('Multiplicacao', multiplicacao);

##retorna um número bool
divisao = 10 / 3;
print('Divisão', divisao);

exponeciacao = 2 ** 10;
print('Exponeciação', exponeciacao);

## corta as casas decimais
divisao_inteira = 10 // 10;
print('Divisão inteira', divisao_inteira);

modulo = 64 % 8; ##resto da divisão
print('Módulo', modulo);

print(modulo == 0)

